package fr.gurvannbrenne.y2024.day08;

public class Antenna {
    private Integer x;
    private Integer y;

    public Antenna(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }
}
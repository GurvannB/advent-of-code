package fr.gurvannbrenne.y2024.day01;

public class Couple {
    private Integer a;
    private Integer b;

    public Couple(Integer a, Integer b) {
        this.a = a;
        this.b = b;
    }

    public Integer getA() {
        return a;
    }

    public Integer getB() {
        return b;
    }
}
